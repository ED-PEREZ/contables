    private void acciones(int i, boolean b) {
        bAgregar.setEnabled(b);
        bEliminar.setEnabled(b);
        bGuardar.setEnabled(b);
        bLimpiar.setEnabled(b);
        bSalir.setEnabled(b);
        tfecha.setEnabled(b);
        tfecha.setDate(new Date());
        tCuenta.setEnabled(b);
        nCuenta.setEnabled(b);
        tMonto.setEnabled(b);
        tColumna.setEnabled(b);
        tiva.setEnabled(b);
        tConcepto.setEnabled(b);
        bNueva.setEnabled(!b);
        if (i == 2 || i == 3 || i == 4) {
            tCuenta.setText("");
            nCuenta.setText("");
            tMonto.setText("");
            tColumna.setSelectedIndex(0);
            tiva.setSelectedIndex(0);
            civa.setSelectedIndex(0);
            if (i == 2 || i == 4) {
                tnpartida.setText("");
                tConcepto.setText("");
                if (i == 4) {
                    tfecha.setDate(null);
                }
            }
        }
    }

    private void numP() {
        pda = cp.numeroPartida();
        if (pda == null) {
            tnpartida.setText("1");
        } else {
            tnpartida.setText("" + pda.getNumpartida() + 1);
        }
    }

    private boolean validarA() {
        boolean b = false;
        if (tnpartida.getText() == "" || tMonto.getText() == "" || tColumna.getSelectedIndex() == 0
                || (tiva.getSelectedIndex() != 0 && civa.getSelectedIndex() == 0)) {
            b = false;
            JOptionPane.showMessageDialog(rootPane, "COMPLETE/SELECCIONE LOS CAMPOS CORRESPONDIENTES", "FALTAN DATOS", HEIGHT);
        } else {
            b = true;
        }
        return b;
    }

